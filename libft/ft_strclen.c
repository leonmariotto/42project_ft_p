#include <string.h>

size_t		ft_strclen(const char *s, const char c)
{
	size_t i;

	i = 0;
	while (s[i] != '\0' && s[i] != c)
	{
		i++;
	}
	if (c == '\0' && s[i] == '\0')
		i++;
	return (i);
}
